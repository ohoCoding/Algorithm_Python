# 문자열 자료형
##1. 큰따옴표로("")로 양쪽 둘러싸기
print("Hello World")
##2. 작은따옴표('')로 양쪽 둘러싸기
print("Python is fun")
##3. 큰따옴표 3개를 연속(""")으로 써서 양쪽 둘러싸기
print("""Life is too short, You need python""")
##4. 작은 따옴표 3개를 연속(''')으로 써서 양쪽 둘러싸기
print("""Life is too short, You need python""")

# 문자열 안에 작은 따옴표나 큰따옴표를 포함시킬때
